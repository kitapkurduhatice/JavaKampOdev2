package homeWork2;

public class Student extends User {
	
	private String schoolNumber;
	private String gradeClass;
	private String schollName;
	private int percentOfSuccess;
	

	public String getSchoolNumber() {
		return schoolNumber;
	}

	public void setSchoolNumber(String schoolNumber) {
		this.schoolNumber = schoolNumber;
	}

	public String getGradeClass() {
		return gradeClass;
	}

	public void setGradeClass(String gradeClass) {
		this.gradeClass = gradeClass;
	}

	public String getSchollName() {
		return schollName;
	}

	public void setSchollName(String schollName) {
		this.schollName = schollName;
	}

	public int getPercentOfSuccess() {
		return percentOfSuccess;
	}

	public void setPercentOfSuccess(int percentOfSuccess) {
		this.percentOfSuccess = percentOfSuccess;
	}
	
	
	
	

}
